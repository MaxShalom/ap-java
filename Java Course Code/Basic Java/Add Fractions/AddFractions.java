public class AddFractions extends ConsoleProgram
{
    public void run()
    {
        int none = readInt("What is the numerator of the first fraction?");
        int done = readInt("What is the denominator of the first fraction?");
        int ntwo = readInt("What is the numerator of the second fraction?");
        int dtwo = readInt("What is the denominator of the second fraction?");
        System.out.print = none / done;
        //System.out.print = +;
        System.out.print = ntwo / dtwo;
    }
}
